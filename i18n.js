const CONTACT_EMAIL = 'iammarfr2000@gmail.com';

function applyLang(lang) {
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;
  document.documentElement.dir = dict._dir;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) el.textContent = dict[key];
  });

  // Boutons mailto avec sujet/corps traduits
  const bookBtn = document.getElementById('bookBtn');
  const askBtn = document.getElementById('askBtn');
  if (bookBtn) bookBtn.href = `mailto:${CONTACT_EMAIL}?subject=${encodeURIComponent(dict.mail_book_subject)}&body=${encodeURIComponent(dict.mail_book_body)}`;
  if (askBtn) askBtn.href = `mailto:${CONTACT_EMAIL}?subject=${encodeURIComponent(dict.mail_ask_subject)}`;
  const waBtn = document.getElementById('waBtn');
  if (waBtn) waBtn.href = `https://wa.me/21623208208?text=${encodeURIComponent(dict.wa_msg)}`;

  document.querySelectorAll('#langSwitch button').forEach((b) => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  try { localStorage.setItem('darsekia_lang', lang); } catch (e) {}
}

document.querySelectorAll('#langSwitch button').forEach((b) => {
  b.addEventListener('click', () => applyLang(b.dataset.lang));
});

// Langue initiale : préférence sauvegardée > langue du navigateur > français
let initial = 'fr';
try { initial = localStorage.getItem('darsekia_lang') || ''; } catch (e) {}
if (!initial) {
  const nav = (navigator.language || 'fr').slice(0, 2);
  initial = ['fr', 'en', 'de', 'ar'].includes(nav) ? nav : 'fr';
}
applyLang(initial);
