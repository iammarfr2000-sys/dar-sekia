function buildBookingMessage(dict) {
  const name = document.getElementById('fName').value.trim();
  const phone = document.getElementById('fPhone').value.trim();
  const email = document.getElementById('fEmail').value.trim();
  const ci = document.getElementById('fCheckin').value;
  const co = document.getElementById('fCheckout').value;
  const adults = Number(document.getElementById('cntAdults').textContent);
  const children = Number(document.getElementById('cntChildren').textContent);
  const infants = Number(document.getElementById('cntInfants').textContent);
  const guests = adults + ' ' + dict.form_adults.toLowerCase() + (children ? ', ' + children + ' ' + dict.form_children.toLowerCase() : '') + (infants ? ', ' + infants + ' ' + dict.form_infants.toLowerCase() : '');
  const msg = document.getElementById('fMessage').value.trim();

  if (!name || !phone || !email || !ci || !co) {
    document.getElementById('fError').style.color = '#ffb4b4';
    document.getElementById('fError').textContent = dict.form_error;
    return null;
  }
  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    document.getElementById('fError').style.color = '#ffb4b4';
    document.getElementById('fError').textContent = dict.form_email_invalid;
    return null;
  }
  if (datesConflict(ci, co)) {
    document.getElementById('fError').textContent = dict.form_conflict;
    return null;
  }
  document.getElementById('fError').textContent = '';

  let nights = '';
  const d1 = new Date(ci), d2 = new Date(co);
  if (d2 > d1) nights = Math.round((d2 - d1) / 86400000) + ' ' + dict.form_summary_nights;

  const lines = [
    dict.form_title + ' — Dar Sekia',
    '',
    dict.form_name + ': ' + name,
    dict.form_phone.replace(' *','') + ': ' + phone,
    dict.form_email.replace(' *','') + ': ' + email,
    dict.form_checkin + ': ' + ci,
    dict.form_checkout + ': ' + co + (nights ? ' (' + nights + ')' : ''),
    dict.form_guests + ': ' + guests,
    msg ? '' : null,
    msg || null,
  ].filter((l) => l !== null);

  return lines.join('\n');
}

