(function () {
  const counts = { adults: 2, children: 0, infants: 0 };
  const MAX_GUESTS = 12;

  function refresh() {
    const a = document.getElementById('cntAdults');
    const c = document.getElementById('cntChildren');
    const i = document.getElementById('cntInfants');
    if (!a) return;
    a.textContent = counts.adults;
    c.textContent = counts.children;
    i.textContent = counts.infants;

    const total = counts.adults + counts.children;
    document.querySelectorAll('.stepper-btn').forEach((btn) => {
      const type = btn.dataset.type;
      const dir = Number(btn.dataset.dir);
      let disabled = false;
      if (dir < 0) {
        if (type === 'adults') disabled = counts.adults <= 1;
        else disabled = counts[type] <= 0;
      } else {
        if (type === 'infants') disabled = counts.infants >= 4;
        else disabled = total >= MAX_GUESTS;
      }
      btn.disabled = disabled;
      btn.style.opacity = disabled ? '0.35' : '1';
    });
  }

  document.querySelectorAll('.stepper-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const type = btn.dataset.type;
      counts[type] += Number(btn.dataset.dir);
      refresh();
    });
  });

  refresh();
})();


// ---------- Synchronisation avec l'agenda Google (flux iCal public) ----------
let BOOKED_RANGES = []; // [{start: 'YYYY-MM-DD', end: 'YYYY-MM-DD'}] end exclusif

function icsDateToISO(v) {
  // 20260810 -> 2026-08-10 ; 20260810T140000Z -> 2026-08-10
  const m = v.match(/^(\d{4})(\d{2})(\d{2})/);
  return m ? m[1] + '-' + m[2] + '-' + m[3] : null;
}

function parseICS(text) {
  const ranges = [];
  const events = text.split('BEGIN:VEVENT').slice(1);
  for (const ev of events) {
    const s = ev.match(/DTSTART[^:]*:(\S+)/);
    const e = ev.match(/DTEND[^:]*:(\S+)/);
    if (s) {
      const start = icsDateToISO(s[1]);
      const end = e ? icsDateToISO(e[1]) : start;
      if (start) ranges.push({ start, end: end || start });
    }
  }
  return ranges;
}

function datesConflict(ci, co) {
  return BOOKED_RANGES.some((r) => ci < r.end && r.start < co);
}

(function loadBookedDates() {
  const icsUrl = 'https://calendar.google.com/calendar/ical/1dbd830c3f31946eb36aff1331c8a998365f98b2c479ef59edf978be30ed29f3%40group.calendar.google.com/public/basic.ics';
  const proxied = 'https://api.allorigins.win/raw?url=' + encodeURIComponent(icsUrl);
  fetch(proxied)
    .then((r) => (r.ok ? r.text() : Promise.reject()))
    .then((text) => { BOOKED_RANGES = parseICS(text); })
    .catch(() => { /* Si le chargement échoue, le formulaire fonctionne sans vérification. */ });
})();

// Vérification en direct quand l'utilisateur choisit ses dates
(function liveConflictCheck() {
  const ci = document.getElementById('fCheckin');
  const co = document.getElementById('fCheckout');
  const err = document.getElementById('fError');
  if (!ci || !co) return;
  function check() {
    if (ci.value && co.value && datesConflict(ci.value, co.value)) {
      err.textContent = currentDict().form_conflict;
    } else if (err.textContent && err.textContent.startsWith('⚠️')) {
      err.textContent = '';
    }
  }
  ci.addEventListener('change', check);
  co.addEventListener('change', check);
})();
